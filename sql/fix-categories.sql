-- Переименование тематических разделов в библиотечные категории литературы.
SET client_encoding = 'UTF8';

UPDATE categories
SET name = 'Учебная литература',
    description = 'Учебники, пособия и профессиональная литература для обучения.'
WHERE LOWER(name) IN (
    'программирование', 'programming', 'учебники', 'техническая литература'
);

UPDATE categories
SET name = 'Художественная литература',
    description = 'Романы, повести, поэзия, драматургия.'
WHERE LOWER(name) IN (
    'классика', 'classics', 'художественная', 'художественная литература'
);

UPDATE categories
SET name = 'Научно-популярная литература',
    description = 'Научно-популярные и познавательные издания.'
WHERE LOWER(name) IN (
    'наука', 'science', 'научная литература', 'научно-популярная'
);
